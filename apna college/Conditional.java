public class Conditional {
    public static void main(String[] args) {
        boolean isSunUp = true;
        if(isSunUp==true){
            System.out.println("day");
        }
        else
        {
            System.out.println("night");
        }
        int age =30;
        if(age>30){
            System.out.println("can vote");
        }
        else{
            System.out.println("cannot vote");
        }
    }
}
